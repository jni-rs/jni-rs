/**
 * Annotations for customizing Rust bindings generation with jbindgen.
 *
 * <p>
 * This package contains annotations that can be applied to Java source code
 * to control how the jbindgen tool generates Rust JNI bindings. These
 * annotations
 * are processed during binding generation and do not introduce runtime
 * dependencies.
 *
 * <p>
 * Key annotations:
 * <ul>
 * <li>{@link io.github.jni_rs.jbindgen.RustName} - Override generated Rust
 * names</li>
 * </ul>
 *
 * @since 0.1.0
 */
package io.github.jni_rs.jbindgen;
